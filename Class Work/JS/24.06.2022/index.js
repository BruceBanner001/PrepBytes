//OPERATORS

//Arithmetic Operators
console.log(10 + 5);    //15
console.log(10 - 5);    //5
console.log(10 * 5);    //50
console.log(10 / 5);    //2
console.log(10 % 5);    //0

console.log("End Arithmetic Operators")

//Comparison Operators
console.log(2 < 3);     //true
console.log(2 > 3);     //false
console.log(2 <= 3);     //true
console.log(2 >= 3);     //false

// == (double equals) or != and === (triple equals) or !==

//double equals or != check only the values and not the data type.
//triple equals or !== check both the values and the data type.

console.log('== and ===')

console.log(3 == '3')   //true
console.log(3 === '3')   //false
console.log(3 === 3 )   //true
console.log(3 === 3)   //true

console.log(3 != '3')   //false
console.log(3 !== '3')  //true
console.log(3 !== 3 )   //false
console.log(3 !== 3)   //false
console.log('raja' !== '5') //true
console.log('raja' !== 'raja') //false
console.log(1 !== '1')  //true

console.log("End of Comparision Operators")

//Bitwise Operators

// & (And), | (OR)

console.log(2 & 3);
console.log(2 | 3);